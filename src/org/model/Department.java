package org.model;

public class Department {
	private int deparNo;
	private String deparName;
	private String deparTel;
	public int getDeparNo() {
		return deparNo;
	}
	public void setDeparNo(int deparNo) {
		this.deparNo = deparNo;
	}
	public String getDeparName() {
		return deparName;
	}
	public void setDeparName(String deparName) {
		this.deparName = deparName;
	}
	public String getDeparTel() {
		return deparTel;
	}
	public void setDeparTel(String deparTel) {
		this.deparTel = deparTel;
	}
}
